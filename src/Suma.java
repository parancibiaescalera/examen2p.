import java.sql.SQLOutput;

public class Suma {
    void sumar(int valorA, int valorB){
        System.out.println("la suma de dos valores es:"+(valorA+valorB));
    }
    void sumar(int valorA, int valorB, int valorC){
        System.out.println("la suma de tres valores es:"+(valorA+valorB+valorC));
    }
    void sumar(double valorA, double valorB){
        System.out.println("la suma dos valores con decimales es:"+(valorA+valorB));
    }
    void sumar(double valorA, double valorB, double valorC){
        System.out.println("la suma tres valores con decimales es:"+(valorA+valorB+valorC));
    }
}
